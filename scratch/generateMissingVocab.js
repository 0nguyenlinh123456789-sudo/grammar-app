const generateFullWordObject = (en, type, vi, ipa, exampleBase, viExampleBase, synonyms = '', antonyms = '') => {
  return {
    en: en,
    type: type,
    vi: vi,
    ipa: ipa,
    example: exampleBase,
    viExample: viExampleBase,
    synonyms: synonyms,
    antonyms: antonyms
  };
};

const updateFile = (filePath, arrayName, dataList, fileImportHeader = '') => {
  console.log(`Processing file: ${filePath}`);
  
  // Update words in memory
  dataList.forEach(topic => {
    const extraWords = additionalWordsDb[topic.id];
    if (extraWords && extraWords.length > 0) {
      const currentWordSet = new Set(topic.words.map(w => w.en.toLowerCase()));
      let addedCount = 0;
      
      extraWords.forEach(([en, type, vi, ipa, ex, viEx, syn, ant]) => {
        if (!currentWordSet.has(en.toLowerCase())) {
          // Construct proper sentences
          const fullEx = ex.includes(' ') ? ex : `This is a common example for ${en}.`;
          const fullViEx = viEx.includes(' ') ? viEx : `Đây là một ví dụ phổ biến cho từ ${vi}.`;
          
          const wordObj = generateFullWordObject(
            en, 
            type, 
            vi, 
            ipa, 
            fullEx.charAt(0).toUpperCase() + fullEx.slice(1), 
            fullViEx.charAt(0).toUpperCase() + fullViEx.slice(1), 
            syn || '', 
            ant || ''
          );
          topic.words.push(wordObj);
          currentWordSet.add(en.toLowerCase());
          addedCount++;
        }
      });
      console.log(`   Topic: ${topic.id} - Added ${addedCount} words. New Total: ${topic.words.length}`);
    }
  });

  // Write file back
  const formattedContent = `${fileImportHeader}
const ${arrayName} = ${JSON.stringify(dataList, null, 2)};
export default ${arrayName};
`;
  fs.writeFileSync(filePath, formattedContent, 'utf8');
  console.log(`Successfully updated and saved ${filePath}!`);
};

